<?php
/**
 * Editor-only WordPress stubs for static analysis in this workspace.
 *
 * This file is not loaded by the theme at runtime; it only exists so
 * Intelephense can resolve common WordPress symbols in extracted templates.
 */

if ( false ) {
    function __( $text, $domain = 'default' ) {
        return $text;
    }

    function _n( $single, $plural, $number, $domain = 'default' ) {
        return $number === 1 ? $single : $plural;
    }

    function esc_attr( $text ) {
        return $text;
    }

    function esc_attr__( $text, $domain = 'default' ) {
        return $text;
    }

    function esc_html( $text ) {
        return $text;
    }

    function esc_html__( $text, $domain = 'default' ) {
        return $text;
    }

    function esc_url( $url, $protocols = null, $_context = 'display' ) {
        return $url;
    }

    function get_stylesheet_directory() {
        return '';
    }

    function get_the_title( $post = 0 ) {
        return '';
    }

    function home_url( $path = '', $scheme = null ) {
        return '';
    }

    function number_format_i18n( $number, $decimals = 0 ) {
        return (string) $number;
    }
